export const useTranslation = (language: Language) => {
  const t = translations[language];
  
  const d = (value: string | undefined): string => {
    if (!value) return '';
    if (language === 'en') return value;
    return t.data[value as keyof typeof t.data] || value;
  };

  return { t, d };
};
